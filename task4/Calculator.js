class Calculator {
    static addition(a,b) {
        return a+b
    }

    static subtraction(a,b){
        return a-b
    }

    static multiplication(a,b){
        return a*b
    }

    static division(a,b){
        return a/b
    }

}

console.log(Calculator.addition(3,3))
console.log(Calculator.subtraction(3,3))
console.log(Calculator.multiplication(3,3))
console.log(Calculator.division(3,3))